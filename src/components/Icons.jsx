export const getResourceTypeIcon = (type, size = 20, className = '') => {
    switch (type) {
        case 'doc':
            return <FileTextIcon size={size} className={className} />;
        case 'video':
            return <VideoIcon size={size} className={className} />;
        case 'link':
            return <LinkIcon size={size} className={className} />;
        case 'file':
            return <FolderIcon size={size} className={className} />;
        case 'image':
            return <FileTextIcon size={size} className={className} />;
        default:
            return <FileTextIcon size={size} className={className} />;
    }
};
